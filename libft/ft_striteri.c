void				ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;
	int				z;

	z = 0;
	i = 0;
	if (s && f && *s)
		while (s[z] != '\0')
		{
			f(i, &s[z]);
			i++;
			z++;
		}
}

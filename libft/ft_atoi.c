int		ft_atoi(char *str)
{
	int				i;
	unsigned long	a;
	int				d;

	i = 0;
	a = 0;
	d = 1;
	while (str[i] == 32 || (str[i] <= 13 && str[i] >= 9))
		i++;
	if (str[i] == 43 || str[i] == 45)
	{
		if (str[i] == 45)
			d = -1;
		i++;
	}
	while ((str[i] >= 48 && str[i] <= 57))
	{
		a = a * 10 + (int)str[i] - 48;
		i++;
		if (a >= 9223372036854775807 && d == 1)
			return (-1);
		else if (a > 9223372036854775807 && d == -1)
			return (0);
	}
	return (int)(a * d);
}
